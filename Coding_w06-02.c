#include <stdio.h>

int main() {
    // --- ส่วนที่ 1: ตัวแปรจำนวนเต็ม ---
    int i = 1;
    int j = 2;
    int k = 0; // กำหนดค่าเริ่มต้นให้ k เป็นศูนย์

    printf("W06-02-01:\n");
    printf("1. เริ่มต้น -> i = %d, j = %d, k = ยังไม่ได้กำหนด\n", i, j);

    // คำนวณค่าผลรวมของ i และ j แล้วเก็บใน k
    k = i + j;
    printf("2. หลังจาก k = i + j -> i = %d, j = %d, k = %d\n", i, j, k);

    // คำนวณ i ใหม่ โดยนำค่าปัจจุบันของ k มาคูณ 2 แล้วบวกกับ i
    i += k * 2;
    printf("3. หลังจาก i = i + (k * 2) -> i = %d, j = %d, k = %d\n", i, j, k);

    // j เท่ากับผลหารของ i ด้วย 2 (หารแบบจำนวนเต็ม)
    j = i / 2;
    printf("4. หลังจาก j = i / 2 -> i = %d, j = %d, k = %d\n", i, j, k);

    // k เท่ากับเศษจากการหารของ i ด้วย 2
    k = i % 2;
    printf("5. หลังจาก k = i %% 2 -> i = %d, j = %d, k = %d\n", i, j, k);

    // คำนวณค่าใหม่ให้ i โดยนำ j + k แล้วคูณ 3
    i = (j + k) * 3;
    printf("6. หลังจาก i = (j + k) * 3 -> i = %d, j = %d, k = %d\n\n", i, j, k);


    // --- ส่วนที่ 2: ตัวแปรทศนิยม ---
    double x = 1.0;
    double y = 2.0;
    double z = 0.0; // กำหนดค่าเริ่มต้นให้ z เป็นศูนย์

    printf("W06-02-02:\n");
    printf("1. เริ่มต้น -> x = %.1f, y = %.1f, z = ยังไม่ได้กำหนด\n", x, y);

    // x เป็นผลรวมของ y กับ 5.0
    x = y + 5.0;
    printf("2. หลังจาก x = y + 5.0 -> x = %.1f, y = %.1f\n", x, y);

    // y เท่ากับ x หารด้วย 2.0
    y = x / 2.0;
    printf("3. หลังจาก y = x / 2.0 -> x = %.1f, y = %.1f\n", x, y);

    // y เท่ากับ (x คูณ 3.0) แล้วบวก 4.0
    y = (x * 3.0) + 4.0;
    printf("4. หลังจาก y = (x * 3.0) + 4.0 -> x = %.1f, y = %.1f\n", x, y);

    // x เท่ากับ -0.5 ลบด้วย y
    x = -0.5 - y;
    printf("5. หลังจาก x = -0.5 - y -> x = %.1f, y = %.1f\n", x, y);

    // z เท่ากับ x + y
    z = x + y;
    printf("6. หลังจาก z = x + y -> x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;
}