#include <stdio.h>

int main() {
    // กำหนดค่าตัวแปรจำนวนเต็ม
    int x = 12;
    int y = 7;
    int z = 12;

    printf("ตรวจสอบเงื่อนไขต่าง ๆ:\n");

    // ข้อ 1: x มากกว่า y หรือไม่
    printf("1. x > y: %d\n", x > y);  // 12 > 7 -> 1 (จริง)

    // ข้อ 2: x น้อยกว่า z หรือไม่
    printf("2. x < z: %d\n", x < z);  // 12 < 12 -> 0 (เท่ากัน ไม่ใช่น้อยกว่า)

    // ข้อ 3: x เท่ากับ z หรือไม่
    printf("3. x == z: %d\n", x == z);  // 12 == 12 -> 1 (จริง)

    // ข้อ 4: x ไม่เท่ากับ y หรือไม่
    printf("4. x != y: %d\n", x != y);  // 12 != 7 -> 1 (จริง)

    // ข้อ 5: ตรวจสอบนิพจน์ทางตรรกะรวม (!, ||)
    int expr1 = !(2 * 5 >= y);       // !(10 >= 7) -> !(1) -> 0
    int expr2 = (5 != (5 / 3));      // 5 / 3 = 1 -> 5 != 1 -> 1
    printf("5. !(2*5 >= y) || (5 != (5/3)): %d\n", expr1 || expr2);  // 0 || 1 = 1

    // ข้อ 6: x ไม่ได้น้อยกว่า y หรือไม่
    printf("6. !(x < y): %d\n", !(x < y));  // !(12 < 7) -> !(0) -> 1

    // ข้อ 7: x + y มากกว่า z * 2 หรือไม่
    printf("7. (x + y) > (z * 2): %d\n", (x + y) > (z * 2));  // 19 > 24 -> 0

    // ข้อ 8: x เป็นเลขคู่ หรือ y เป็นเลขคี่ หรือไม่
    int isXEven = (x % 2 == 0);  // 12 % 2 == 0 -> 1
    int isYOdd  = (y % 2 == 1);  // 7 % 2 == 1 -> 1
    printf("8. x เป็นเลขคู่ || y เป็นเลขคี่: %d\n", isXEven || isYOdd);  // 1 || 1 = 1

    // ข้อ 9: x > y และ z < y หรือไม่
    printf("9. (x > y) && (z < y): %d\n", (x > y) && (z < y));  // 1 && 0 = 0

    return 0;
}