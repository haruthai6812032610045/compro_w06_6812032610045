#include <stdio.h>
#include <math.h>

int main() {
    // ประกาศตัวแปรจำนวนเต็ม a, b และจำนวนจริง c, d
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    // แสดงค่าที่กำหนดให้กับตัวแปรทั้งหมด
    printf("ค่าเริ่มต้นของตัวแปร:\n");
    printf("a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d);

    // คำนวณและแสดงผล d + a
    printf("ผลลัพธ์ d + a = %.1f\n", d + a);  // 4.0 + 5 = 9.0

    // คำนวณและแสดงผล a - b
    printf("ผลลัพธ์ a - b = %d\n", a - b);    // 5 - 17 = -12

    // คำนวณและแสดงผล c * d
    printf("ผลลัพธ์ c * d = %.2f\n", c * d);  // 8.5 * 4.0 = 34.00

    // คำนวณและแสดงผล a * c
    printf("ผลลัพธ์ a * c = %.2f\n", a * c);  // 5 * 8.5 = 42.50

    // คำนวณและแสดงผล c / d
    printf("ผลลัพธ์ c / d = %.2f\n", c / d);  // 8.5 / 4.0 = 2.13

    // คำนวณและแสดงผล b / c
    printf("ผลลัพธ์ b / c = %.2f\n", b / c);  // 17 / 8.5 = 2.00

    // คำนวณและแสดงผล a mod b (หารเอาเศษ)
    printf("ผลลัพธ์ a %% b = %d\n", a % b);   // 5 % 17 = 5

    // แปลง c เป็นจำนวนเต็ม แล้ว mod กับ a
    printf("ผลลัพธ์ ((int)c) %% a = %d\n", ((int)c) % a);  // (int)8.5 = 8 -> 8 % 5 = 3

    // ใช้ฟังก์ชัน fmod หาค่าเศษของ c หารด้วย d แบบจำนวนจริง
    printf("ผลลัพธ์ fmod(c, d) = %.2f\n", fmod(c, d));  // 8.5 % 4.0 = 0.5

    return 0;
}